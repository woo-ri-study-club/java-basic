public class Car {
    private String brand;
    private int year;

    public Car() {
    }

    public void setBrand(final String brand) {
        this.brand = brand;
    }

    public void setYear(final int year) {
        this.year = year;
    }

    public String getBrand() {
        return brand;
    }

    public int getYear() {
        return year;
    }
}
